function add(firstNo, secondNo){
    return parseInt(firstNo) + parseInt(secondNo);
}

function subtract(firstNo, secondNo){
    return parseInt(firstNo) - parseInt(secondNo);
}

function multiply(firstNo, secondNo){
    return parseInt(firstNo) * parseInt(secondNo);
}

function divide(firstNo, secondNo){
    return parseInt(firstNo) / parseInt(secondNo);
}